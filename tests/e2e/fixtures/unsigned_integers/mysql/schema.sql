CREATE TABLE unsigned_values (
    id      BIGINT   UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    u8_val  TINYINT  UNSIGNED NOT NULL,
    u16_val SMALLINT UNSIGNED NOT NULL,
    u24_val MEDIUMINT UNSIGNED NOT NULL,
    u32_val INT      UNSIGNED NOT NULL,
    u64_val BIGINT   UNSIGNED NOT NULL
);
