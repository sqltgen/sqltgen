//! Emission of `__init__.py` re-export files. Kept separate from `core.rs`
//! so the per-file line/function counts stay within the quality ratchet.

use std::fmt::Write;
use std::path::PathBuf;

use crate::backend::GeneratedFile;

/// Emit one `__init__.py` containing the given import lines plus an
/// `__all__ = [...]` re-export list. `subdir` is the package directory
/// (or `""` for the top-level package). Lays out `__all__` one entry per
/// line so pyright's `reportUnusedImport` does not flag the imports.
pub(super) fn build_init_file(out: &str, subdir: &str, imports: &[String], exports: &[String]) -> anyhow::Result<GeneratedFile> {
    let mut src = String::from("# Generated by sqltgen. Do not edit.\n");
    for line in imports {
        writeln!(src, "{line}")?;
    }
    if !exports.is_empty() {
        writeln!(src, "\n__all__ = [")?;
        for name in exports {
            writeln!(src, "    \"{name}\",")?;
        }
        writeln!(src, "]")?;
    }
    let path = PathBuf::from(out).join(subdir).join("__init__.py");
    Ok(GeneratedFile { path, content: src })
}
