pub enum PythonTarget {
    Postgres,
    Sqlite,
    Mysql,
}

impl From<Engine> for PythonTarget {
    fn from(engine: Engine) -> Self {
        match engine {
            Engine::Postgresql => PythonTarget::Postgres,
            Engine::Sqlite => PythonTarget::Sqlite,
            Engine::Mysql => PythonTarget::Mysql,
        }
    }
}

pub struct PythonCodegen {
    pub target: PythonTarget,
}

impl Codegen for PythonCodegen {
    fn generate(&self, schema: &Schema, queries: &[Query], config: &OutputConfig) -> anyhow::Result<Vec<GeneratedFile>> {
        let mut files = Vec::new();

        // One @dataclass file per table
        for table in &schema.tables {
            let class_name = to_pascal_case(&table.name);
            let mut src = String::new();
            writeln!(src, "# Generated by sqltgen. Do not edit.")?;
            writeln!(src, "from __future__ import annotations")?;
            writeln!(src)?;
            writeln!(src, "import dataclasses")?;

            let mut imports = TypeImports::default();
            for col in &table.columns {
                imports.scan(&col.sql_type);
            }
            imports.write(&mut src)?;

            writeln!(src)?;
            writeln!(src)?;
            writeln!(src, "@dataclasses.dataclass")?;
            writeln!(src, "class {class_name}:")?;
            for col in &table.columns {
                let ty = python_type(&col.sql_type, col.nullable, &self.target);
                writeln!(src, "    {}: {}", col.name, ty)?;
            }

            let path = PathBuf::from(&config.out).join(format!("{}.py", table.name));
            files.push(GeneratedFile { path, content: src });
        }

        // One .py file per query group
        let groups = group_queries(queries);
        let mut group_stems: Vec<String> = Vec::new();
        for (group, group_queries) in &groups {
            let stem = queries_file_stem(group).to_string();
            group_stems.push(stem.clone());
            let src = build_queries_file(group_queries, schema, &self.target, config)?;
            let path = PathBuf::from(&config.out).join(format!("{stem}.py"));
            files.push(GeneratedFile { path, content: src });
        }

        // __init__.py
        {
            let mut src = String::new();
            writeln!(src, "# Generated by sqltgen. Do not edit.")?;
            for table in &schema.tables {
                let class_name = to_pascal_case(&table.name);
                writeln!(src, "from .{} import {}", table.name, class_name)?;
            }
            for stem in &group_stems {
                writeln!(src, "from . import {stem}")?;
            }

            let path = PathBuf::from(&config.out).join("__init__.py");
            files.push(GeneratedFile { path, content: src });
        }

        Ok(files)
    }
}

fn build_queries_file(queries: &[Query], schema: &Schema, target: &PythonTarget, config: &OutputConfig) -> anyhow::Result<String> {
    let strategy = config.list_params.clone().unwrap_or_default();
    let mut src = String::new();

    let any_inline_rows = queries.iter().any(|q| has_inline_rows(q, schema));

    // Collect type imports across all query params and result columns
    let mut imports = TypeImports::default();
    for query in queries {
        for p in &query.params {
            imports.scan(&p.sql_type);
        }
        for col in &query.result_columns {
            imports.scan(&col.sql_type);
        }
    }

    // Header
    writeln!(src, "# Generated by sqltgen. Do not edit.")?;
    match target {
        PythonTarget::Postgres => writeln!(src, "# Runtime: psycopg (psycopg3) — pip install psycopg")?,
        PythonTarget::Sqlite => writeln!(src, "# Runtime: sqlite3 (stdlib)")?,
        PythonTarget::Mysql => writeln!(src, "# Runtime: mysql-connector-python — pip install mysql-connector-python")?,
    }
    writeln!(src, "from __future__ import annotations")?;
    writeln!(src)?;
    if any_inline_rows {
        writeln!(src, "import dataclasses")?;
    }
    match target {
        PythonTarget::Postgres => writeln!(src, "import psycopg")?,
        PythonTarget::Sqlite => writeln!(src, "import sqlite3")?,
        PythonTarget::Mysql => writeln!(src, "import mysql.connector")?,
    }
    imports.write(&mut src)?;

    // Table imports (sorted, only tables used as return types)
    let mut needed_tables: BTreeSet<&str> = BTreeSet::new();
    for query in queries {
        if let Some(table_name) = infer_table(query, schema) {
            needed_tables.insert(table_name);
        }
    }
    if !needed_tables.is_empty() {
        writeln!(src)?;
        for table_name in &needed_tables {
            let class_name = to_pascal_case(table_name);
            writeln!(src, "from .{table_name} import {class_name}")?;
        }
    }

    // SQL constants (skipped for dynamic list-param queries — SQL is built at runtime)
    writeln!(src)?;
    for query in queries {
        if query.params.iter().any(|p| p.is_list) && strategy == ListParamStrategy::Dynamic {
            continue;
        }
        let const_name = sql_const_name(&query.name);
        let base_sql = if let Some(lp) = query.params.iter().find(|p| p.is_list) {
            rewrite_list_sql_native(&query.sql, lp, list_rewrite_target(lp, target))
        } else {
            query.sql.clone()
        };
        let sql = normalize_sql(&base_sql, target).replace('"', "\\\"").replace('\n', " ");
        let sql = sql.trim_end().trim_end_matches(';');
        writeln!(src, r#"{const_name} = "{sql}""#)?;
    }

    // Query functions
    for query in queries {
        writeln!(src)?;
        writeln!(src)?;
        if has_inline_rows(query, schema) {
            emit_row_dataclass(&mut src, query, target)?;
            writeln!(src)?;
            writeln!(src)?;
        }
        emit_python_query(&mut src, query, schema, target, &strategy)?;
    }

    Ok(src)
}

fn emit_row_dataclass(src: &mut String, query: &Query, target: &PythonTarget) -> anyhow::Result<()> {
    let name = format!("{}Row", to_pascal_case(&query.name));
    writeln!(src, "@dataclasses.dataclass")?;
    writeln!(src, "class {name}:")?;
    for col in &query.result_columns {
        let ty = python_type(&col.sql_type, col.nullable, target);
        writeln!(src, "    {}: {}", col.name, ty)?;
    }
    Ok(())
}

fn emit_python_query(src: &mut String, query: &Query, schema: &Schema, target: &PythonTarget, strategy: &ListParamStrategy) -> anyhow::Result<()> {
    if let Some(lp) = query.params.iter().find(|p| p.is_list) {
        return emit_python_list_query(src, query, schema, target, strategy, lp);
    }
    match target {
        PythonTarget::Postgres => emit_cursor_query(src, query, schema, "psycopg.Connection", target),
        PythonTarget::Mysql => emit_cursor_query(src, query, schema, "mysql.connector.MySQLConnection", target),
        PythonTarget::Sqlite => emit_sqlite_query(src, query, schema, target),
    }
}

/// Emits a query function using a cursor context manager (`with conn.cursor() as cur:`).
/// Used by both the psycopg (Postgres) and mysql-connector-python (MySQL) targets,
/// which share the same cursor API and `%s` placeholder style.
fn emit_cursor_query(src: &mut String, query: &Query, schema: &Schema, conn_type: &str, target: &PythonTarget) -> anyhow::Result<()> {
    let fn_name = to_snake_case(&query.name);
    let const_name = sql_const_name(&query.name);
    let return_type = cursor_return_type(query, schema);

    let params_sig: String = std::iter::once(format!("conn: {conn_type}"))
        .chain(query.params.iter().map(|p| format!("{}: {}", to_snake_case(&p.name), python_type(&p.sql_type, p.nullable, target))))
        .collect::<Vec<_>>()
        .join(", ");

    writeln!(src, "def {fn_name}({params_sig}) -> {return_type}:")?;
    writeln!(src, "    with conn.cursor() as cur:")?;

    let execute_call = build_execute_call(&const_name, query);
    writeln!(src, "        cur.{execute_call}")?;

    match query.cmd {
        QueryCmd::Exec => {},
        QueryCmd::ExecRows => {
            writeln!(src, "        return cur.rowcount")?;
        },
        QueryCmd::One => {
            let row_type = result_row_type(query, schema);
            writeln!(src, "        row = cur.fetchone()")?;
            writeln!(src, "        if row is None:")?;
            writeln!(src, "            return None")?;
            writeln!(src, "        return {row_type}(*row)")?;
        },
        QueryCmd::Many => {
            let row_type = result_row_type(query, schema);
            writeln!(src, "        return [{row_type}(*row) for row in cur.fetchall()]")?;
        },
    }

    Ok(())
}

fn emit_sqlite_query(src: &mut String, query: &Query, schema: &Schema, target: &PythonTarget) -> anyhow::Result<()> {
    let fn_name = to_snake_case(&query.name);
    let const_name = sql_const_name(&query.name);
    let return_type = cursor_return_type(query, schema);

    let params_sig: String = std::iter::once("conn: sqlite3.Connection".to_string())
        .chain(query.params.iter().map(|p| format!("{}: {}", to_snake_case(&p.name), python_type(&p.sql_type, p.nullable, target))))
        .collect::<Vec<_>>()
        .join(", ");

    writeln!(src, "def {fn_name}({params_sig}) -> {return_type}:")?;

    // sqlite3 uses conn.execute() directly — no cursor context manager
    let execute_call = build_execute_call(&const_name, query);
    match query.cmd {
        QueryCmd::Exec => {
            writeln!(src, "    conn.{execute_call}")?;
        },
        QueryCmd::ExecRows => {
            writeln!(src, "    return conn.{execute_call}.rowcount")?;
        },
        QueryCmd::One => {
            let row_type = result_row_type(query, schema);
            writeln!(src, "    row = conn.{execute_call}.fetchone()")?;
            writeln!(src, "    if row is None:")?;
            writeln!(src, "        return None")?;
            writeln!(src, "    return {row_type}(*row)")?;
        },
        QueryCmd::Many => {
            let row_type = result_row_type(query, schema);
            writeln!(src, "    return [{row_type}(*row) for row in conn.{execute_call}.fetchall()]")?;
        },
    }

    Ok(())
}

/// Emit a query function that has a list parameter.
fn emit_python_list_query(
    src: &mut String,
    query: &Query,
    schema: &Schema,
    target: &PythonTarget,
    strategy: &ListParamStrategy,
    lp: &Parameter,
) -> anyhow::Result<()> {
    let fn_name = to_snake_case(&query.name);
    let const_name = sql_const_name(&query.name);
    let conn_type = match target {
        PythonTarget::Postgres => "psycopg.Connection",
        PythonTarget::Mysql => "mysql.connector.MySQLConnection",
        PythonTarget::Sqlite => "sqlite3.Connection",
    };
    let return_type = cursor_return_type(query, schema);
    let lp_name = to_snake_case(&lp.name);
    let scalar_params: Vec<&Parameter> = query.params.iter().filter(|p| !p.is_list).collect();

    let params_sig: String = std::iter::once(format!("conn: {conn_type}"))
        .chain(query.params.iter().map(|p| {
            let ty = if p.is_list { format!("list[{}]", python_type(&p.sql_type, false, target)) } else { python_type(&p.sql_type, p.nullable, target) };
            format!("{}: {ty}", to_snake_case(&p.name))
        }))
        .collect::<Vec<_>>()
        .join(", ");

    writeln!(src, "def {fn_name}({params_sig}) -> {return_type}:")?;

    match (target, strategy) {
        (PythonTarget::Postgres, ListParamStrategy::Native) => {
            // psycopg3 accepts a Python list for = ANY(%s)
            let scalar_args = build_scalar_args(&scalar_params, lp, &lp_name);
            writeln!(src, "    with conn.cursor() as cur:")?;
            writeln!(src, "        cur.execute({const_name}, {scalar_args})")?;
            emit_python_result_block(src, query, schema, "cur", "        ")?;
        },
        (PythonTarget::Sqlite, ListParamStrategy::Native) => {
            writeln!(src, "    import json")?;
            writeln!(src, "    {lp_name}_json = json.dumps({lp_name})")?;
            let scalar_args = build_scalar_args(&scalar_params, lp, &format!("{lp_name}_json"));
            writeln!(src, "    cur = conn.execute({const_name}, {scalar_args})")?;
            emit_python_result_block(src, query, schema, "cur", "    ")?;
        },
        (PythonTarget::Mysql, ListParamStrategy::Native) => {
            // mysql-connector: pass JSON string
            writeln!(src, "    import json")?;
            writeln!(src, "    {lp_name}_json = json.dumps({lp_name})")?;
            let scalar_args = build_scalar_args(&scalar_params, lp, &format!("{lp_name}_json"));
            writeln!(src, "    with conn.cursor() as cur:")?;
            writeln!(src, "        cur.execute({const_name}, {scalar_args})")?;
            emit_python_result_block(src, query, schema, "cur", "        ")?;
        },
        (PythonTarget::Postgres | PythonTarget::Mysql, ListParamStrategy::Dynamic) => {
            let (before_raw, after_raw) = split_at_in_clause(&query.sql, lp.index).unwrap_or_else(|| (query.sql.clone(), String::new()));
            let before = normalize_sql(&before_raw, target).replace('"', "\\\"").replace('\n', " ");
            let after = normalize_sql(&after_raw, target).replace('"', "\\\"").replace('\n', " ");
            let args_expr = build_dynamic_args(&scalar_params, lp);
            writeln!(src, "    placeholders = \", \".join([\"%s\"] * len({lp_name}))")?;
            writeln!(src, "    sql = f\"{before}IN ({{placeholders}}){after}\"")?;
            writeln!(src, "    with conn.cursor() as cur:")?;
            writeln!(src, "        cur.execute(sql, {args_expr})")?;
            emit_python_result_block(src, query, schema, "cur", "        ")?;
        },
        (PythonTarget::Sqlite, ListParamStrategy::Dynamic) => {
            let (before_raw, after_raw) = split_at_in_clause(&query.sql, lp.index).unwrap_or_else(|| (query.sql.clone(), String::new()));
            let before = rewrite_to_anon_params(&before_raw).replace('"', "\\\"").replace('\n', " ");
            let after = rewrite_to_anon_params(&after_raw).replace('"', "\\\"").replace('\n', " ");
            let args_expr = build_dynamic_args(&scalar_params, lp);
            writeln!(src, "    placeholders = \", \".join([\"?\"] * len({lp_name}))")?;
            writeln!(src, "    sql = f\"{before}IN ({{placeholders}}){after}\"")?;
            writeln!(src, "    cur = conn.execute(sql, {args_expr})")?;
            emit_python_result_block(src, query, schema, "cur", "    ")?;
        },
    }

    Ok(())
}

/// Emit the result-reading block for a Python query.
fn emit_python_result_block(src: &mut String, query: &Query, schema: &Schema, cursor: &str, indent: &str) -> anyhow::Result<()> {
    match query.cmd {
        QueryCmd::Exec => {},
        QueryCmd::ExecRows => writeln!(src, "{indent}return {cursor}.rowcount")?,
        QueryCmd::One => {
            let row_type = result_row_type(query, schema);
            writeln!(src, "{indent}row = {cursor}.fetchone()")?;
            writeln!(src, "{indent}if row is None:")?;
            writeln!(src, "{indent}    return None")?;
            writeln!(src, "{indent}return {row_type}(*row)")?;
        },
        QueryCmd::Many => {
            let row_type = result_row_type(query, schema);
            writeln!(src, "{indent}return [{row_type}(*row) for row in {cursor}.fetchall()]")?;
        },
    }
    Ok(())
}

/// Build argument tuple for execute call: scalar params, then list param.
fn build_scalar_args(scalar_params: &[&Parameter], lp: &Parameter, lp_expr: &str) -> String {
    // Build ordered args (scalar params in order, then lp_expr at lp.index position).
    // For native strategy, the list param slot gets the list/json expression.
    // We reconstruct occurrence order: scalar params by index before lp.index, then lp, then after.
    let mut args: Vec<String> = scalar_params.iter().map(|p| to_snake_case(&p.name)).collect();
    // Insert lp_expr at its position (by index order)
    let insert_pos = scalar_params.iter().filter(|p| p.index < lp.index).count();
    args.insert(insert_pos, lp_expr.to_string());
    build_arg_tuple(&args)
}

/// Build a Python argument tuple string: `(a,)` for one, `(a, b, c)` for many, `()` for none.
fn build_arg_tuple(args: &[String]) -> String {
    match args.len() {
        0 => "()".to_string(),
        1 => format!("({},)", args[0]),
        _ => format!("({})", args.join(", ")),
    }
}

/// Build the args expression for dynamic list strategy, respecting SQL occurrence order.
///
/// Scalars before the list param come first, then `tuple(lp)`, then scalars after.
fn build_dynamic_args(scalar_params: &[&Parameter], lp: &Parameter) -> String {
    let lp_name = to_snake_case(&lp.name);
    let before: Vec<String> = scalar_params.iter().filter(|p| p.index < lp.index).map(|p| to_snake_case(&p.name)).collect();
    let after: Vec<String> = scalar_params.iter().filter(|p| p.index > lp.index).map(|p| to_snake_case(&p.name)).collect();
    let before_tuple = build_arg_tuple(&before);
    let after_tuple = build_arg_tuple(&after);
    match (before.is_empty(), after.is_empty()) {
        (true, true) => format!("tuple({lp_name})"),
        (false, true) => format!("{before_tuple} + tuple({lp_name})"),
        (true, false) => format!("tuple({lp_name}) + {after_tuple}"),
        (false, false) => format!("{before_tuple} + tuple({lp_name}) + {after_tuple}"),
    }
}

/// Build the [`ListRewriteTarget`] for the current Python database target.
fn list_rewrite_target(lp: &Parameter, target: &PythonTarget) -> ListRewriteTarget {
    match target {
        PythonTarget::Postgres => ListRewriteTarget::PgArray,
        PythonTarget::Sqlite => ListRewriteTarget::JsonEach("?".to_string()),
        PythonTarget::Mysql => ListRewriteTarget::JsonTable { placeholder: "%s".to_string(), col_type: mysql_json_table_col_type(&lp.sql_type).to_string() },
    }
}

fn build_execute_call(const_name: &str, query: &Query) -> String {
    if query.params.is_empty() {
        return format!("execute({const_name})");
    }
    // Build args in SQL occurrence order so reused params appear multiple times,
    // matching the positional %s / ? placeholders in the rewritten SQL.
    let args: Vec<String> = positional_bind_names(query).iter().map(|&n| to_snake_case(n)).collect();
    let tuple = if args.len() == 1 { format!("({},)", args[0]) } else { format!("({})", args.join(", ")) };
    format!("execute({const_name}, {tuple})")
}

fn cursor_return_type(query: &Query, schema: &Schema) -> String {
    match query.cmd {
        QueryCmd::One => format!("{} | None", result_row_type(query, schema)),
        QueryCmd::Many => format!("list[{}]", result_row_type(query, schema)),
        QueryCmd::Exec => "None".to_string(),
        QueryCmd::ExecRows => "int".to_string(),
    }
}

// ─── Type helpers ─────────────────────────────────────────────────────────────

fn python_type(sql_type: &SqlType, nullable: bool, target: &PythonTarget) -> String {
    let base = match sql_type {
        SqlType::Boolean => "bool".to_string(),
        SqlType::SmallInt | SqlType::Integer | SqlType::BigInt => "int".to_string(),
        SqlType::Real | SqlType::Double => "float".to_string(),
        SqlType::Decimal => "decimal.Decimal".to_string(),
        SqlType::Text | SqlType::Char(_) | SqlType::VarChar(_) => "str".to_string(),
        SqlType::Bytes => "bytes".to_string(),
        SqlType::Date => "datetime.date".to_string(),
        SqlType::Time => "datetime.time".to_string(),
        SqlType::Timestamp | SqlType::TimestampTz => "datetime.datetime".to_string(),
        SqlType::Interval => "datetime.timedelta".to_string(),
        SqlType::Uuid => "uuid.UUID".to_string(),
        // psycopg3 automatically deserializes JSON/JSONB → Python objects.
        // sqlite3 and mysql-connector return raw JSON text.
        SqlType::Json | SqlType::Jsonb => match target {
            PythonTarget::Postgres => "object".to_string(),
            PythonTarget::Sqlite | PythonTarget::Mysql => "str".to_string(),
        },
        SqlType::Array(inner) => {
            let inner_ty = python_type(inner, false, target);
            let list_ty = format!("list[{inner_ty}]");
            return if nullable { format!("{list_ty} | None") } else { list_ty };
        },
        SqlType::Custom(_) => "Any".to_string(),
    };
    if nullable {
        format!("{base} | None")
    } else {
        base
    }
}

#[derive(Default)]
struct TypeImports {
    decimal: bool,
    datetime: bool,
    uuid: bool,
    any_type: bool,
}

impl TypeImports {
    fn scan(&mut self, ty: &SqlType) {
        match ty {
            SqlType::Decimal => self.decimal = true,
            SqlType::Date | SqlType::Time | SqlType::Timestamp | SqlType::TimestampTz | SqlType::Interval => self.datetime = true,
            SqlType::Uuid => self.uuid = true,
            // Json/Jsonb map to builtins (object or str) — no import needed
            SqlType::Json | SqlType::Jsonb => {},
            SqlType::Custom(_) => self.any_type = true,
            SqlType::Array(inner) => self.scan(inner),
            _ => {},
        }
    }

    fn write(&self, src: &mut String) -> anyhow::Result<()> {
        if self.decimal {
            writeln!(src, "import decimal")?;
        }
        if self.datetime {
            writeln!(src, "import datetime")?;
        }
        if self.uuid {
            writeln!(src, "import uuid")?;
        }
        if self.any_type {
            writeln!(src, "from typing import Any")?;
        }
        Ok(())
    }
}

// ─── SQL helpers ──────────────────────────────────────────────────────────────

fn normalize_sql(sql: &str, target: &PythonTarget) -> String {
    match target {
        // Both psycopg and mysql-connector-python use %s positional placeholders
        PythonTarget::Postgres | PythonTarget::Mysql => rewrite_to_percent_s(sql),
        PythonTarget::Sqlite => rewrite_to_anon_params(sql),
    }
}

// ─── Row helpers ──────────────────────────────────────────────────────────────

fn result_row_type(query: &Query, schema: &Schema) -> String {
    infer_row_type_name(query, schema).unwrap_or_else(|| "Any".to_string())
}

