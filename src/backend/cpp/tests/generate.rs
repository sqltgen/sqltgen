// ─── generate: includes sorted (BTreeSet) ────────────────────────────────

#[test]
fn test_generate_includes_are_sorted() {
    // A table that needs <cstdint>, <optional>, <string> — check they appear in order.
    let schema = Schema::with_tables(vec![user_table()]);
    let files = pg().generate(&schema, &[], &cfg()).unwrap();
    let src = get_file(&files, "user.hpp");
    let cstdint_pos = src.find("#include <cstdint>").unwrap();
    let optional_pos = src.find("#include <optional>").unwrap();
    let string_pos = src.find("#include <string>").unwrap();
    assert!(cstdint_pos < optional_pos, "<cstdint> should come before <optional>");
    assert!(optional_pos < string_pos, "<optional> should come before <string>");
}

