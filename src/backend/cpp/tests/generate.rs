#[test]
fn test_generate_namespace_when_package_set() {
    let schema = Schema::with_tables(vec![user_table()]);
    let config = OutputConfig { out: "out".to_string(), package: "mydb".to_string(), list_params: None, ..Default::default() };
    let files = pg().generate(&schema, &[], &config).unwrap();
    let src = get_file(&files, "user.hpp");
    assert!(src.contains("namespace mydb {"));
    assert!(src.contains("} // namespace mydb"));
}

// ─── query files: basic structure ───────────────────────────────────────

#[test]
fn test_generate_query_header_and_source_files() {
    let schema = Schema::default();
    let query = Query::exec("DeleteUser", "DELETE FROM user WHERE id = $1", vec![Parameter::scalar(1, "id", SqlType::BigInt, false)]);
    let files = pg().generate(&schema, &[query], &cfg()).unwrap();
    let _ = get_file(&files, "queries.hpp");
    let src = get_file(&files, "queries.cpp");
    assert!(src.contains("#include \"queries.hpp\""));
    assert!(src.contains("// Generated by sqltgen. Do not edit."));
}

// ─── query files: readability / header layout ───────────────────────────

#[test]
fn test_queries_header_groups_inline_row_sql_and_declaration_per_query() {
    let schema = Schema::with_tables(vec![user_table()]);
    let query = Query::one(
        "GetUserName",
        "SELECT name FROM user WHERE id = $1",
        vec![Parameter::scalar(1, "id", SqlType::BigInt, false)],
        vec![ResultColumn::not_nullable("name", SqlType::Text)],
    );
    let files = pg().generate(&schema, &[query], &cfg()).unwrap();
    let src = get_file(&files, "queries.hpp");

    let struct_pos = src.find("struct GetUserNameRow {").expect("inline row struct");
    let sql_pos = src.find("inline const std::string SQL_GET_USER_NAME").expect("sql constant");
    let decl_pos = src.find("std::optional<GetUserNameRow> get_user_name").expect("function decl");

    assert!(struct_pos < sql_pos);
    assert!(sql_pos < decl_pos);
}

#[test]
fn test_queries_header_sql_constants_use_multiline_raw_strings() {
    let schema = Schema::default();
    let query = Query::exec(
        "DeleteUser",
        "DELETE FROM \"user\" WHERE id = $1",
        vec![Parameter::scalar(1, "id", SqlType::BigInt, false)],
    );
    let files = pg().generate(&schema, &[query], &cfg()).unwrap();
    let src = get_file(&files, "queries.hpp");

    assert!(src.contains("inline const std::string SQL_DELETE_USER = R\"sql(\n"));
    assert!(src.contains("DELETE FROM \"user\" WHERE id = $1\n)sql\";"));
    assert!(!src.contains("inline const std::string SQL_DELETE_USER = R\"sql(DELETE FROM"));
}

#[test]
fn test_queries_header_includes_table_header_for_reused_result_type() {
    let schema = Schema::with_tables(vec![user_table()]);
    let query = Query::one(
        "GetUser",
        "SELECT id, name, bio FROM user WHERE id = $1",
        vec![Parameter::scalar(1, "id", SqlType::BigInt, false)],
        vec![
            ResultColumn::not_nullable("id", SqlType::BigInt),
            ResultColumn::not_nullable("name", SqlType::Text),
            ResultColumn::nullable("bio", SqlType::Text),
        ],
    )
    .with_source_table(Some("user".to_string()));
    let files = pg().generate(&schema, &[query], &cfg()).unwrap();
    let src = get_file(&files, "queries.hpp");
    assert!(src.contains("#include \"user.hpp\""));
    assert!(src.contains("std::optional<User> get_user(pqxx::connection& db, const std::int64_t& id);"));
}

// ─── querier wrapper ────────────────────────────────────────────────────

#[test]
fn test_generate_querier_wrapper() {
    let schema = Schema::default();
    let query = Query::exec("DeleteUser", "DELETE FROM user WHERE id = $1", vec![Parameter::scalar(1, "id", SqlType::BigInt, false)]);
    let files = pg().generate(&schema, &[query], &cfg()).unwrap();
    let header = get_file(&files, "queries.hpp");
    assert!(header.contains("class Querier {"));
    assert!(header.contains("pqxx::connection& db_;"));
    assert!(header.contains("explicit Querier(pqxx::connection& db) : db_(db) {}"));
    assert!(header.contains("void delete_user(const std::int64_t& id);"));
}
