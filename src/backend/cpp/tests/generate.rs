use super::*;

// ─── generate: table struct file ─────────────────────────────────────────

#[test]
fn test_generate_table_struct() {
    let schema = Schema::with_tables(vec![user_table()]);
    let files = pg().generate(&schema, &[], &cfg()).unwrap();
    let src = get_file(&files, "user.hpp");
    assert!(src.contains("struct User {"), "should emit PascalCase struct name");
    assert!(src.contains("std::int64_t id;"), "non-nullable bigint → int64_t");
    assert!(src.contains("std::string name;"), "non-nullable text → string");
    assert!(src.contains("std::optional<std::string> bio;"), "nullable text → optional<string>");
}

