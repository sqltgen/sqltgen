#[test]
fn test_generate_no_includes_for_bool_only() {
    let table = Table {
        name: "flag".to_string(),
        kind: TableKind::Table,
        columns: vec![
            Column { name: "active".to_string(), sql_type: SqlType::Boolean, nullable: false, is_primary_key: false },
        ],
    };
    let schema = Schema::with_tables(vec![table]);
    let files = pg().generate(&schema, &[], &cfg()).unwrap();
    let src = get_file(&files, "flag.hpp");
    // bool needs no includes
    assert!(!src.contains("#include"), "bool-only table should have no includes");
    // Struct should follow directly after the header comment
    assert!(src.contains("struct Flag {"));
    assert!(src.contains("bool active;"));
}

