// ─── table files ────────────────────────────────────────────────────────

#[test]
fn test_generate_table_struct() {
    let schema = Schema::with_tables(vec![user_table()]);
    let files = pg().generate(&schema, &[], &cfg()).unwrap();
    let src = get_file(&files, "user.hpp");
    assert!(src.contains("#pragma once"));
    assert!(src.contains("// Generated by sqltgen. Do not edit."));
    assert!(src.contains("struct User {"));
    assert!(src.contains("std::int64_t id;"));
    assert!(src.contains("std::string name;"));
    assert!(src.contains("std::optional<std::string> bio;"));
}

