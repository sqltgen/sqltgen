#[test]
fn test_generate_namespace_when_package_set() {
    let schema = Schema::with_tables(vec![user_table()]);
    let config = OutputConfig { out: "out".to_string(), package: "mydb".to_string(), list_params: None, ..Default::default() };
    let files = pg().generate(&schema, &[], &config).unwrap();
    let src = get_file(&files, "user.hpp");
    assert!(src.contains("namespace mydb {"), "should open namespace from package");
    assert!(src.contains("} // namespace mydb"), "should close namespace with comment");
}

// ─── generate: multiple tables ───────────────────────────────────────────

#[test]
fn test_generate_one_file_per_table() {
    let schema = Schema::with_tables(vec![
        user_table(),
        Table {
            name: "post".to_string(),
            kind: TableKind::Table,
            columns: vec![
                Column { name: "id".to_string(), sql_type: SqlType::Integer, nullable: false, is_primary_key: true },
                Column { name: "title".to_string(), sql_type: SqlType::Text, nullable: false, is_primary_key: false },
            ],
        },
    ]);
    let files = pg().generate(&schema, &[], &cfg()).unwrap();
    assert_eq!(files.len(), 2);
    let _ = get_file(&files, "user.hpp");
    let post_src = get_file(&files, "post.hpp");
    assert!(post_src.contains("struct Post {"));
    assert!(post_src.contains("std::int32_t id;"));
    assert!(post_src.contains("std::string title;"));
}

